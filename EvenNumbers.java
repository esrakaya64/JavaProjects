
package evennumbers;


public class EvenNumbers {

   
    public static void main(String[] args) {
      //We want to numbers can be divided by 2 without remainder
      //We should a stop number for stop loop
      for(int i =0; i<89; i++){
          if(i%2 == 00){
              System.out.println(i);
          }
      }
        
    }
    
}
