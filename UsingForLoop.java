
package usingforloop;


public class UsingForLoop {

    
    public static void main(String[] args) {
        /*Basit bir for döngüsü kullanımı
        Burada 1'den 15'e kadar olan sayıları yazdırmak istiyoruz.
        Öncelikle değişkenimize 1 Değerini veriyoruz.
        Değişkenimiz 16'dan küçük olduğu sürece devamlı artsın istiyoruz ve ekrana yazdırıyoruz.*/
        for(int i = 1; i<16; i++)
            System.out.println(i);
    }
    
}
